fun main (args: Array<String>) {

    /** Explore Data Types in Kotlin */

    var name: String
    name = "Heet"

    var age: Int = 10

    var myAge = 20

    var yourAge: Int
    yourAge = 30

    var isAlive: Boolean = true
    var marks: Float = 97.4F
    var percentage: Double = 90.78
    var generator: Char = 'M'

    println(name)
    println(age)
    println(myAge)
    println(yourAge)
    println(isAlive)
    println(marks)
    println(percentage)
    println(generator)
}